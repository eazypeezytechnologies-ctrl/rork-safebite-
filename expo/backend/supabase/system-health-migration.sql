-- Function to create a new health run
CREATE OR REPLACE FUNCTION public.start_health_run(
  p_environment TEXT DEFAULT 'production',
  p_trigger_type TEXT DEFAULT 'scheduled',
  p_triggered_by UUID DEFAULT NULL
)
RETURNS UUID AS $$
DECLARE
  v_run_id UUID;
BEGIN
  INSERT INTO public.system_health_runs (environment, trigger_type, triggered_by)
  VALUES (p_environment, p_trigger_type, p_triggered_by)
  RETURNING id INTO v_run_id;
  
  RETURN v_run_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Function to complete a health run
CREATE OR REPLACE FUNCTION public.complete_health_run(
  p_run_id UUID,
  p_status TEXT,
  p_summary TEXT DEFAULT NULL
)
RETURNS VOID AS $$
BEGIN
  UPDATE public.system_health_runs
  SET 
    finished_at = NOW(),
    status = p_status,
    summary_text = p_summary
  WHERE id = p_run_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Function to log a health check result
CREATE OR REPLACE FUNCTION public.log_health_check(
  p_run_id UUID,
  p_check_name TEXT,
  p_category TEXT,
  p_status TEXT,
  p_duration_ms INTEGER DEFAULT NULL,
  p_details JSONB DEFAULT '{}',
  p_error_message TEXT DEFAULT NULL
)
RETURNS UUID AS $$
DECLARE
  v_check_id UUID;
BEGIN
  INSERT INTO public.system_health_checks (run_id, check_name, check_category, status, duration_ms, details, error_message)
  VALUES (p_run_id, p_check_name, p_category, p_status, p_duration_ms, p_details, p_error_message)
  RETURNING id INTO v_check_id;
  
  RETURN v_check_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Function to log a system action
CREATE OR REPLACE FUNCTION public.log_system_action(
  p_run_id UUID,
  p_action_name TEXT,
  p_action_type TEXT,
  p_status TEXT,
  p_target_entity TEXT DEFAULT NULL,
  p_details JSONB DEFAULT '{}',
  p_error_message TEXT DEFAULT NULL
)
RETURNS UUID AS $$
DECLARE
  v_action_id UUID;
BEGIN
  INSERT INTO public.system_actions (run_id, action_name, action_type, status, target_entity, details, error_message, completed_at)
  VALUES (
    p_run_id, 
    p_action_name, 
    p_action_type, 
    p_status, 
    p_target_entity, 
    p_details, 
    p_error_message,
    CASE WHEN p_status IN ('success', 'failed', 'skipped') THEN NOW() ELSE NULL END
  )
  RETURNING id INTO v_action_id;
  
  RETURN v_action_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

