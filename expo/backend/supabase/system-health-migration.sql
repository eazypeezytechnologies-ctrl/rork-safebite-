-- Auto-update updated_at for job_events
CREATE OR REPLACE FUNCTION update_job_events_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trigger_job_events_updated_at
  BEFORE UPDATE ON public.job_events
  FOR EACH ROW
  EXECUTE FUNCTION update_job_events_updated_at();

