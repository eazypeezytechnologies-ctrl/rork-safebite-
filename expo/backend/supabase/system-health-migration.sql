-- =============================================
-- SafeBite System Health & Auto-Remediation Tables
-- Migration for self-scan, monitoring, and audit trail
-- =============================================

-- =============================================
-- 1. SYSTEM HEALTH RUNS
-- Main table tracking each health check execution
-- =============================================
CREATE TABLE IF NOT EXISTS public.system_health_runs (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  started_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
  finished_at TIMESTAMP WITH TIME ZONE,
  status TEXT NOT NULL DEFAULT 'running' CHECK (status IN ('running', 'pass', 'warn', 'fail')),
  environment TEXT NOT NULL DEFAULT 'production' CHECK (environment IN ('development', 'preview', 'production')),
  app_version TEXT,
  trigger_type TEXT NOT NULL DEFAULT 'scheduled' CHECK (trigger_type IN ('scheduled', 'manual', 'webhook')),
  triggered_by UUID REFERENCES auth.users(id),
  summary_text TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- =============================================
-- 2. SYSTEM HEALTH CHECKS
-- Individual check results within a run
-- =============================================
CREATE TABLE IF NOT EXISTS public.system_health_checks (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  run_id UUID NOT NULL REFERENCES public.system_health_runs(id) ON DELETE CASCADE,
  check_name TEXT NOT NULL,
  check_category TEXT NOT NULL DEFAULT 'general' CHECK (check_category IN ('auth', 'database', 'api', 'cache', 'queue', 'storage', 'external', 'general')),
  status TEXT NOT NULL CHECK (status IN ('pass', 'warn', 'fail', 'skip')),
  duration_ms INTEGER,
  details JSONB DEFAULT '{}',
  error_message TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- =============================================
-- 3. SYSTEM ACTIONS
-- Auto-remediation actions taken
-- =============================================
CREATE TABLE IF NOT EXISTS public.system_actions (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  run_id UUID REFERENCES public.system_health_runs(id) ON DELETE SET NULL,
  action_name TEXT NOT NULL,
  action_type TEXT NOT NULL DEFAULT 'remediation' CHECK (action_type IN ('remediation', 'cleanup', 'notification', 'escalation')),
  status TEXT NOT NULL CHECK (status IN ('pending', 'running', 'success', 'failed', 'skipped')),
  target_entity TEXT,
  details JSONB DEFAULT '{}',
  error_message TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  completed_at TIMESTAMP WITH TIME ZONE
);

-- =============================================
-- 4. SYSTEM ALERTS
-- Alerts generated by health checks
-- =============================================
CREATE TABLE IF NOT EXISTS public.system_alerts (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  run_id UUID REFERENCES public.system_health_runs(id) ON DELETE SET NULL,
  severity TEXT NOT NULL CHECK (severity IN ('low', 'medium', 'high', 'critical')),
  category TEXT NOT NULL DEFAULT 'system',
  title TEXT NOT NULL,
  message TEXT NOT NULL,
  details JSONB DEFAULT '{}',
  acknowledged_at TIMESTAMP WITH TIME ZONE,
  acknowledged_by UUID REFERENCES auth.users(id),
  resolved_at TIMESTAMP WITH TIME ZONE,
  resolved_by UUID REFERENCES auth.users(id),
  resolution_notes TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- =============================================
-- 5. APP ERROR EVENTS
-- Client and server error tracking
-- =============================================
CREATE TABLE IF NOT EXISTS public.app_error_events (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID REFERENCES auth.users(id) ON DELETE SET NULL,
  source TEXT NOT NULL CHECK (source IN ('client', 'server', 'edge_function', 'webhook')),
  severity TEXT NOT NULL DEFAULT 'error' CHECK (severity IN ('debug', 'info', 'warning', 'error', 'critical')),
  error_type TEXT,
  message TEXT NOT NULL,
  stack_hash TEXT,
  stack_trace TEXT,
  component TEXT,
  metadata JSONB DEFAULT '{}',
  device_info JSONB DEFAULT '{}',
  session_id TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- =============================================
-- 6. JOB EVENTS
-- Queue job tracking
-- =============================================
CREATE TABLE IF NOT EXISTS public.job_events (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  queue_name TEXT NOT NULL,
  job_type TEXT NOT NULL,
  job_id TEXT,
  status TEXT NOT NULL CHECK (status IN ('pending', 'processing', 'completed', 'failed', 'dead_letter')),
  priority INTEGER DEFAULT 0,
  attempts INTEGER DEFAULT 0,
  max_attempts INTEGER DEFAULT 3,
  last_error TEXT,
  payload_hash TEXT,
  metadata JSONB DEFAULT '{}',
  scheduled_for TIMESTAMP WITH TIME ZONE,
  started_at TIMESTAMP WITH TIME ZONE,
  completed_at TIMESTAMP WITH TIME ZONE,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- =============================================
-- 7. SYSTEM CONFIGURATION
-- Runtime configuration flags (e.g., circuit breakers)
-- =============================================
CREATE TABLE IF NOT EXISTS public.system_config (
  key TEXT PRIMARY KEY,
  value JSONB NOT NULL DEFAULT '{}',
  description TEXT,
  updated_by UUID REFERENCES auth.users(id),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- =============================================
